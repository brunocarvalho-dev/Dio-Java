public class Incrementos {
    public static void main(String[] args) {
        // ordem para incremento

        boolean verdadeiro = true;
        int num = 5;

        System.out.println("Valor inicial - " + num);
        System.out.println("Valor incremento pós número \nImpreme depois incrementa - " + num++);
        System.out.println("Valor INCREMENTADO - " + num);
        System.out.println("Valor incremento antes numer\nO valor e incrmenta depois impresso - " + ++num);

        System.out.println(verdadeiro);
        System.out.println("Negando boolean com ! - " + !verdadeiro);

    }
}
